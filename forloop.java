
//This program simulates drinking 5 glasses of water using a for loop that repeats 5 times.

public class forloop {

    public static void main(String[] args) {
        System.out.println("Drinking 5 glasses of water:");

        for (int glass = 1; glass <= 5; glass++) {
            System.out.println("Drank glass " + glass);
        }
    }
}

